upsert_words as (
  insert into public.words (text, reading_level, part_of_speech, syllables)
  select distinct
    m.word_text,
    m.reading_level::int,
    m.part_of_speech::public.part_of_speech,
    m.syllables::int
  from mappings m
  on conflict (text) do update
    set
      reading_level = least(public.words.reading_level, excluded.reading_level),
      part_of_speech = case
        when excluded.part_of_speech <> 'other'::public.part_of_speech then excluded.part_of_speech
        else public.words.part_of_speech
      end
  returning id, text
)

insert into public.word_sounds (word_id, sound_id, position, sequence_index)
select
  w.id,
  s.id,
  m.position::public.sound_position,
  0
from mappings m
join public.sounds s on s.code = m.sound_code
join upsert_words w on w.text = m.word_text
on conflict (word_id, sound_id, position, sequence_index) do nothing;

